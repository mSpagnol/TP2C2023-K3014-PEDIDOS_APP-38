CREATE PROCEDURE BI_PEDIDOS_APP.MIGRAR_BI_HECHO_ENVIO_MENSAJERIA
AS
	BEGIN 
		INSERT INTO BI_PEDIDOS_APP.BI_HECHO_ENVIO_MENSAJERIA(
			ENVIO_MENSAJERIA_ENTREGA,
			ENVIO_MENSAJERIA_FECHA,
			LOCALIDAD_ID,
