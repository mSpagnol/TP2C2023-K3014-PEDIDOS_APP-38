			JOIN PEDIDOS_APP.PAQUETE p on p.PAQUETE_NRO = e.PAQUETE_NRO
			JOIN BI_PEDIDOS_APP.BI_TIPO_PAQUETE tp on tp.PAQUETE_TIPO = p.PAQUETE_TIPO
